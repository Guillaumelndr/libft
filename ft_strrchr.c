#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int i;

	i = 0;
	while (s[i])
		i++;
	while (s[i] != c && i)
		i--;
	if (i == 0 && s[i] != c)
		return (NULL);
	return ((char *)s + i);
}
