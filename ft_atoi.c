#include "libft.h"

int		ft_atoi(const char *str)
{
	long long		res;
	long long		neg;

	res = 0;
	neg = 1;
	while (*str == ' ' || (*str >= 9 && *str <= 13))
		str++;
	if (*str == '-' || *str == '+')
		if (*str++ == '-')
			neg = -1;
	while (*str >= '0' && *str <= '9')
		res = (res * 10) + (*str++ - 48);
	return ((long long)(res * neg));
}
